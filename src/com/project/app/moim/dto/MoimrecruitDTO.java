package com.project.app.moim.dto;

public class MoimrecruitDTO {
	private int recruitnum;
	private String recruittitle;
	private String userid;
	private String recruitcontents;
	private int readcnt;
	private int moimnum;
	private String regdate;
	private int dislike;
	
	public MoimrecruitDTO() {
	}

	public int getRecruitnum() {
		return recruitnum;
	}
	public void setRecruitnum(int recruitnum) {
		this.recruitnum = recruitnum;
	}
	public String getRecruittitle() {
		return recruittitle;
	}
	public void setRecruittitle(String recruittitle) {
		this.recruittitle = recruittitle;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getRecruitcontents() {
		return recruitcontents;
	}
	public void setRecruitcontents(String recruitcontents) {
		this.recruitcontents = recruitcontents;
	}
	public int getReadcnt() {
		return readcnt;
	}
	public void setReadcnt(int readcnt) {
		this.readcnt = readcnt;
	}
	public int getMoimnum() {
		return moimnum;
	}
	public void setMoimnum(int moimnum) {
		this.moimnum = moimnum;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public int getDislike() {
		return dislike;
	}
	public void setDislike(int dislike) {
		this.dislike = dislike;
	}
}
