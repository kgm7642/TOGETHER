package com.project.app.moim.dto;

public class PlaceDTO {	   
	private int placenum;
	private double placelat;
	private double placelon;
	private String placename;
	private String placeaddr;
	private String placeroadaddr;
	private String placecategory;
	private String placephonenumber;
	
	public PlaceDTO() {
	}

	public int getPlacenum() {
		return placenum;
	}

	public void setPlacenum(int placenum) {
		this.placenum = placenum;
	}

	public double getPlacelat() {
		return placelat;
	}

	public void setPlacelat(double placelat) {
		this.placelat = placelat;
	}

	public double getPlacelon() {
		return placelon;
	}

	public void setPlacelon(double placelon) {
		this.placelon = placelon;
	}

	public String getPlacename() {
		return placename;
	}

	public void setPlacename(String placename) {
		this.placename = placename;
	}

	public String getPlaceaddr() {
		return placeaddr;
	}

	public void setPlaceaddr(String placeaddr) {
		this.placeaddr = placeaddr;
	}

	public String getPlaceroadaddr() {
		return placeroadaddr;
	}

	public void setPlaceroadaddr(String placeroadaddr) {
		this.placeroadaddr = placeroadaddr;
	}

	public String getPlacecategory() {
		return placecategory;
	}

	public void setPlacecategory(String placecategory) {
		this.placecategory = placecategory;
	}

	public String getPlacephonenumber() {
		return placephonenumber;
	}

	public void setPlacephonenumber(String placephonenumber) {
		this.placephonenumber = placephonenumber;
	}


}
